# Criar a classe chamada "Calculadora"
class Calculadora:

    # Criar o método chamado 'soma':
    def soma(self, n1, n2):         # 'SELF' é como se fosse o 'THIS' em Java POO
        return n1 + n2
    
    def subtracao(self, n1, n2):  
        return n1 - n2
    
    def multiplicacao(self, n1, n2):  
        return n1 * n2
    
    def divisao(self, n1, n2):  
        return n1 / n2       